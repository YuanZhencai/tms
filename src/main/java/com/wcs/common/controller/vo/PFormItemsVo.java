package com.wcs.common.controller.vo;

import java.io.Serializable;

public class PFormItemsVo implements Serializable {
	private static final long serialVersionUID = -4130765098294519403L;
	private String nachn;
	private String pernr;
	private String email;

	public String getNachn() {
		return nachn;
	}

	public void setNachn(String nachn) {
		this.nachn = nachn;
	}

	public String getPernr() {
		return pernr;
	}

	public void setPernr(String pernr) {
		this.pernr = pernr;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

}
