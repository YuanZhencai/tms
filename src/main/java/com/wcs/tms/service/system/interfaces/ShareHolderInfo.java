package com.wcs.tms.service.system.interfaces;

/**
 * 股东VO对象
 * 
 * @author liushengbin
 * 
 */
public class ShareHolderInfo {

	/**
	 * 股东所在公司ID
	 */
	private Long companyId;
	/**
	 * 股东ID
	 */
	private Long shareHolderId;
	/**
	 * 股东名称
	 */
	private String shareHolderName;
	/**
	 * 资金币别
	 */
	private String currency;
	/**
	 * 股东注册金额
	 */
	private Double fondsTotal;
	/**
	 * 股权比例
	 */
	private Double equityPerc;
	/**
	 * 实际股权比例
	 */
	private Double realEquityPerc;
	/**
	 * 股东已到位资金
	 */
	private Double fondsInPlace;

	/**
	 * 股东未到位资金
	 */
	private Double fondsNotInPlace;

	public Long getCompanyId() {
		return companyId;
	}

	public void setCompanyId(Long companyId) {
		this.companyId = companyId;
	}

	public Long getShareHolderId() {
		return shareHolderId;
	}

	public void setShareHolderId(Long shareHolderId) {
		this.shareHolderId = shareHolderId;
	}

	public String getShareHolderName() {
		return shareHolderName;
	}

	public void setShareHolderName(String shareHolderName) {
		this.shareHolderName = shareHolderName;
	}

	public String getCurrency() {
		return currency;
	}

	public void setCurrency(String currency) {
		this.currency = currency;
	}

	public Double getFondsTotal() {
		return fondsTotal;
	}

	public void setFondsTotal(Double fondsTotal) {
		this.fondsTotal = fondsTotal;
	}

	public Double getEquityPerc() {
		return equityPerc;
	}

	public void setEquityPerc(Double equityPerc) {
		this.equityPerc = equityPerc;
	}

	public Double getRealEquityPerc() {
		return realEquityPerc;
	}

	public void setRealEquityPerc(Double realEquityPerc) {
		this.realEquityPerc = realEquityPerc;
	}

	public Double getFondsInPlace() {
		return fondsInPlace;
	}

	public void setFondsInPlace(Double fondsInPlace) {
		this.fondsInPlace = fondsInPlace;
	}

	public Double getFondsNotInPlace() {
		return fondsNotInPlace;
	}

	public void setFondsNotInPlace(Double fondsNotInPlace) {
		this.fondsNotInPlace = fondsNotInPlace;
	}

}
