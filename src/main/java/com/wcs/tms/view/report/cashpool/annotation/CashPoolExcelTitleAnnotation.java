@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.FIELD)
public @interface CashPoolExcelTitleAnnotation {
	// excel导出时标题显示的名字，如果没有设置Annotation属性，将不会被导出和导入
	public String titleName();
	public String columnNO();
}
